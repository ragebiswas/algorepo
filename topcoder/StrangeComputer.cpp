class StrangeComputer {
public:
	int setMemory(string mem) {
		int c = 0;
		int i = 0;
		while(mem[i]=='0' && i<mem.size()) i++;
		
		for(; i<mem.size(); i++) {
			char ch = mem[i];
			while(mem[i]==ch && i<mem.size()) i++;
			i--; c++;
		}
		return c;
	}
};


