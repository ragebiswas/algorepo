class TheEncryptionDivTwo {
public:
	string encrypt(string message) {
		vector<int> m(200, 0);
		int curcipher = 'a';
		string enc = "";
		for(int i=0; i<message.size(); i++) {
			char c = message[i];
			if(m[c]!=0) enc += (char)m[c];
			else {
				m[c] = curcipher++;
				enc += (char)m[c];
			}
		}
		return enc;
	}
};


